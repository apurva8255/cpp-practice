#include <iostream>
using namespace std;

int main(int argc, char const *argv[])
{
  int num1, num2;
  cout<<"enter the value of num1: \n"; //this is insertion operator
  cin>>num1;// this is called extraction operator
  cout<<"enter the value of num2: \n"; //this is insertion operator
  cin>>num2;

  cout<<"the sum of num1 and num2 is: "<<num1+num2;
  return 0;
}
